def _load_velocitee(path: Path) -> vne_config.VelociteeFileConfig:
    try:
        return vne_config.load_file(path)
    except vne_config.ConfigError as exc:
        fatal(str(exc))


def _load_and_validate_vme_manifest(path: Path) -> dict[str, Any]:
    if not path.exists():
        fatal(
            f"VME manifest not found: {path}",
            hint="re-run vme deploy or pass the correct --manifest path.",
        )
    try:
        with open(path) as fh:
            data = json.load(fh)
    except json.JSONDecodeError as exc:
        fatal(f"VME manifest is not valid JSON: {exc}")

    schema = json.loads(_VME_MANIFEST_SCHEMA.read_text())
    validator = jsonschema.Draft7Validator(schema)
    errors = sorted(validator.iter_errors(data), key=lambda e: list(e.path))
    if errors:
        msg = "VME manifest failed schema validation:\n" + "\n".join(
            f"  - {'.'.join(str(p) for p in e.path) or '(root)'}: {e.message}"
            for e in errors
        )
        fatal(msg)
    return data


def _validate_against_vne_schema(manifest: dict[str, Any]) -> None:
    schema = json.loads(_VNE_MANIFEST_SCHEMA.read_text())
    validator = jsonschema.Draft7Validator(schema)
    errors = sorted(validator.iter_errors(manifest), key=lambda e: list(e.path))
    if errors:
        # This is internal: if it triggers, VNE wrote something its own schema
        # doesn't accept. Loud failure is correct.
        msg = "VNE output manifest failed self-validation:\n" + "\n".join(
            f"  - {'.'.join(str(p) for p in e.path) or '(root)'}: {e.message}"
            for e in errors
        )
        fatal(msg)


def _build_vne_record(
    intent: VNEIntent,
    outputs: dict[str, Any],
    vme_data: dict[str, Any],
    provisioner: str,
) -> dict[str, Any]:
    """Assemble the engines.vne entry for the output manifest."""
    return {
        "provisioner": provisioner,
        "opnsense": {
            "vm_ip": outputs.get("opnsense_ip", ""),
            "vmid": intent.opnsense.vm.vmid,
            "api_endpoint": outputs.get(
                "opnsense_api_endpoint", f"https://{outputs.get('opnsense_ip', '')}",
            ),
            "version": intent.opnsense.version,
        },
        "vlans": [
            {
                "id": v.id,
                "name": v.name,
                "cidr": v.cidr,
                "gateway": v.gateway,
                "dhcp_range": (
                    [v.dhcp_start, v.dhcp_end] if v.dhcp_start and v.dhcp_end else None
                ),
            }
            for v in intent.network.vlans
        ],
        "dns": {
            "server": outputs.get("opnsense_ip", ""),
            "domain": intent.network.dns.domain,
            "upstream": list(intent.network.dns.upstream),
        },
        "verification": {
            "passed": True,
            "checks": {
                "api_reachable": True,
                "dns_resolving": True,
                "internet_egress": True,
                "vlans_up": True,
            },
        },
        "proxmox_host": {
            "ip": vme_data["target"]["ip"],
            "hostname": vme_data["target"]["hostname"],
        },
    }


def main() -> None:
    run_app(app)


if __name__ == "__main__":
    main()
