"""VNE entry point.

This is the *only* place that orchestrates a full VNE deployment. The shell
wrapper at scripts/deploy.sh exists solely to call this module — it does not
contain logic.

Flow:
  1. Pre-flight: every required env var present? Halt with one combined error.
  2. Read + JSON-Schema-validate the VME manifest. (Hard fail if VME didn't
     succeed; we are not going to start configuring an unfinished host.)
  3. Read + Pydantic-validate velocitee.yml.
  4. Confirm the named provisioner is registered.
  5. Translate the user config into the internal VNEIntent.
  6. Build the renderer pipeline from the registry, run it.
  7. Run verify.py — gate. If anything fails here, do *not* write a manifest.
  8. Append the engines.vne record to the VME manifest and persist.

Path B (join existing network instead of provisioning a new OPNsense): not
implemented. Exit 1 with a clear message if requested.
"""

from __future__ import annotations

import json
import logging
import os
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import jsonschema
import typer

# We are vne.deploy — we depend on the shared library and the local vne package.
from shared import manifest as mf
from shared import renderer_registry
from shared.cli import fatal, run_app, warn
from shared.pipeline import Pipeline
from shared.schema import VNEIntent

from vne import config as vne_config
from vne.scripts import verify as vne_verify

log = logging.getLogger("vne.deploy")

VNE_VERSION = "0.1.0"

_REPO_ROOT = Path(__file__).parent
_VME_MANIFEST_SCHEMA = _REPO_ROOT / "schema" / "vme-manifest.schema.json"
_VNE_MANIFEST_SCHEMA = _REPO_ROOT / "schema" / "vne-manifest.schema.json"

def _required_env_for(provisioner: str) -> list[str]:
    """Look up every required env var across the renderers a provisioner uses.

    Each renderer ships its own `config_keys` descriptor. Composite provisioners
    (e.g. opentofu+ansible) span multiple renderers; we union their required
    keys so pre-flight surfaces every miss in one error.
    """
    renderer_registry.ensure_loaded()
    if not renderer_registry.is_registered(provisioner):
        return []
    keys: list[str] = []
    seen: set[str] = set()
    for cls in renderer_registry.lookup(provisioner):
        for env in cls.required_env():
            if env not in seen:
                keys.append(env)
                seen.add(env)
    return keys


