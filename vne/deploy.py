app = typer.Typer(
    name="vne",
    help="VNE — Velocitee Network Configuration Engine.",
    add_completion=False,
    context_settings={"help_option_names": ["-h", "--help"]},
    no_args_is_help=True,
)


@app.command()
def setup(
    manifest: Path = typer.Option(
        ..., "--manifest", "-m",
        help="Path to the VME handoff manifest. Used to pre-fill velocitee.yml.",
    ),
    config: Path = typer.Option(
        Path("velocitee.yml"), "--config", "-c",
        help="Where to write the velocitee.yml file.",
    ),
) -> None:
    """Bootstrap a velocitee.yml from a VME handoff manifest.

    Today this is a thin scaffold — it copies a starter velocitee.yml next to
    the user's working directory and points them at it. The interactive
    wizard that VME has for itself isn't ported to VNE yet.
    """
    if not manifest.exists():
        fatal(f"VME manifest not found: {manifest}")
    if config.exists():
        warn(f"{config} already exists — not overwriting")
        typer.echo(f"Edit {config}, then run: vne deploy --manifest {manifest}")
        return

    starter = (
        "# velocitee.yml — generated by `vne setup`\n"
        "# Fill in the values below, then run:\n"
        f"#   vne deploy --manifest {manifest}\n\n"
        "velocitee:\n"
        '  provisioner: "velocitee-native"\n\n'
        "vne:\n"
        '  wan_interface: "ens18"\n'
        '  lan_interface: "ens19"\n'
        '  opnsense_version: "24.7"\n'
        '  opnsense_iso_url: ""           # required\n'
        '  opnsense_iso_checksum: ""      # required: "sha256:<hex>"\n'
        "  vlans: []\n"
        "  dns:\n"
        '    upstream: ["1.1.1.1"]\n'
        '    domain: "lab.local"\n'
        "  firewall:\n"
        '    default_policy: "block"\n'
        "  opnsense_vm:\n"
        "    vmid: 100\n"
        "    cores: 2\n"
        "    memory_mb: 2048\n"
        "    disk_gb: 20\n"
        '    storage_pool: "local-lvm"\n'
    )
    config.write_text(starter)
    typer.echo(f"Wrote starter config: {config}")
    typer.echo(f"Edit it, then run: vne deploy --manifest {manifest}")


@app.command()
def deploy(
    config: Path = typer.Option(
        Path("velocitee.yml"), "--config", "-c",
        help="Path to velocitee.yml.",
    ),
    vme_manifest: Path = typer.Option(
        ..., "--manifest", "-m",
        help="Path to the VME handoff manifest written by `vme deploy`.",
    ),
    work_dir: Path = typer.Option(
        Path("vne/work"), "--work-dir",
        help="Where renderers stage rendered artifacts (HCL, inventory, …).",
    ),
    state_dir: Path = typer.Option(
        Path("vne/state"), "--state-dir",
        help="Renderer state files (vne.state.json) — survives between runs.",
    ),
    output: Path = typer.Option(
        Path("vne/output"), "--output",
        help="Where the final VNE manifest is written.",
    ),
    path_b: bool = typer.Option(
        False, "--join-existing",
        help="Path B: join an existing network instead of provisioning OPNsense. (Not implemented.)",
    ),
    verbose: bool = typer.Option(False, "--verbose", "-v"),
) -> None:
    """Provision and configure a network on top of a VME-provisioned host."""
    if path_b:
        typer.echo("Path B (join existing network) not yet implemented.", err=True)
        raise typer.Exit(1)

    logging.basicConfig(
        level=logging.DEBUG if verbose else logging.INFO,
        format="%(asctime)s [%(name)s] %(levelname)s %(message)s",
    )

    typer.echo("VNE — Velocitee Network Configuration Engine")
    typer.echo()

    # Step 1 — pre-flight env vars (driven by each renderer's config_keys)
    typer.echo("[1/7] Pre-flight: environment variables ...")
    file_cfg = _load_velocitee(config)
    provisioner = file_cfg.velocitee.provisioner
    expected = _required_env_for(provisioner)
    missing = [k for k in expected if not os.environ.get(k)]
    if missing:
        fatal(
            "missing required environment variables:\n  - " + "\n  - ".join(missing),
            hint="set them in your shell or a .envrc; never commit them.",
        )

    # Step 2 — VME manifest
    typer.echo("[2/7] Validating VME handoff manifest ...")
    vme_data = _load_and_validate_vme_manifest(vme_manifest)
    if vme_data["engines"].get("vme", {}).get("status") != "success":
        fatal(
            "VME manifest does not record a successful run — refusing to proceed.",
            hint=f"check {vme_manifest} or re-run vme deploy.",
        )

    # Step 3 — VNE config
    typer.echo("[3/7] Validating velocitee.yml VNE block ...")
    try:
        vne_config.validate_provisioner(provisioner)
    except vne_config.ConfigError as exc:
        fatal(str(exc))

    proxmox_host = vme_data["target"]["ip"]
    intent: VNEIntent = vne_config.to_intent(file_cfg, proxmox_host=proxmox_host)

    # Step 4 — build pipeline
    typer.echo(f"[4/7] Building renderer pipeline for provisioner '{provisioner}' ...")
    renderer_classes = renderer_registry.lookup(provisioner)
    renderers = []
    work_dir.mkdir(parents=True, exist_ok=True)
    state_dir.mkdir(parents=True, exist_ok=True)
    for cls in renderer_classes:
        renderers.append(cls(intent=intent, work_dir=work_dir, state_dir=state_dir))

    pipeline = Pipeline(renderers)

    # Step 5 — run
    started_at = datetime.now(timezone.utc)
    typer.echo("[5/7] Running pipeline ...")
    result = pipeline.run()

    if not result.success:
        fatal(
            f"pipeline failed: {result.failure_reason}",
            hint="state has been saved to vne/state/vne.state.json — re-running is safe (idempotent).",
        )

    # Step 6 — verify
    typer.echo("[6/7] Verification ...")
    opnsense_ip = result.aggregated_outputs.get("opnsense_ip")
    if not opnsense_ip:
        fatal("renderer pipeline did not return an opnsense_ip output; cannot verify.")

    api_key = (
        os.environ.get("OPNSENSE_API_KEY")
        or result.aggregated_outputs.get("opnsense_api_key")
    )
    api_secret = os.environ.get("OPNSENSE_API_SECRET")

    passed, _checks = vne_verify.run_all(
        opnsense_ip=opnsense_ip,
        expected_vlan_ids=[v.id for v in intent.network.vlans],
        api_key=api_key,
        api_secret=api_secret,
    )
    if not passed:
        fatal(
            "verification failed — VNE manifest WILL NOT be written.",
            hint="resolve the failure above and re-run; idempotency makes re-runs safe.",
        )

    # Step 7 — write output manifest
    typer.echo("[7/7] Writing VNE output manifest ...")
    completed_at = datetime.now(timezone.utc)

    extra = _build_vne_record(intent, result.aggregated_outputs, vme_data, provisioner)
    manifest = mf.append_engine(
        dict(vme_data),
        engine="vne",
        version=VNE_VERSION,
        started_at=started_at,
        completed_at=completed_at,
        extra=extra,
    )

    # Validate against the VNE-output schema *before* writing.
    _validate_against_vne_schema(manifest)

    output.mkdir(parents=True, exist_ok=True)
    out_path = mf.write(manifest, output)
    typer.echo(f"      wrote {out_path}")

    typer.echo()
    typer.echo("VNE complete. Hand off to VSE with:")
    typer.echo(f"  vse deploy --manifest {out_path}")


